/***********************************************************
 * Arithmatic operations like Addition, Subtraction, Multiplication and division
**********************************************************/
#include"Arithmetic_op.h"

/**********************************************************
 * @name- int Add(int iNum1,int iNum2)
 * @date 23/sept/2022
 * tested-ok
 * @details This function will return the addition of two numbers
 * @param int first integer value
 * @param int second integer value
**********************************************************/
int Add(int iNum1,int iNum2)
{    return  iNum1 + iNum2;     }

/**********************************************************
 * @name- int Sub(int iNum1,int iNum2)
 * @date 23/sept/2022
 * tested-ok
 * @details: This function will return the subtraction of two numbers
 * @param int first integer value
 * @param int second integer value
**********************************************************/
int Sub(int iNum1,int iNum2)
{    return  iNum1 - iNum2;     }
/**********************************************************
 * @name- int Mul(int iNum1,int iNum2)
 * @date 23/sept/2022
 * @test-ok
 * @details: This function will return the multiplication of two numbers
 * @param int first integer value
 * @param int second integer value
**********************************************************/
int Mul(int iNum1,int iNum2)
{    return  iNum1 * iNum2;     }
/**********************************************************
 * @name- int Div(int iNum1,int iNum2)
 * @date 23/sept/2022
 * tested-ok
 * @details: This function will return the division of two numbers
 * @param int first integer value
 * @param int second integer value
**********************************************************/
int Div(int iNum1,int iNum2)
{    return  iNum1 / iNum2;     }
